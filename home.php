<h4 class="btn-primary">Barang</h4>
<p>Terdapat fitur tambah barang, Ubah barang, dan Hapus barang. Perhatikan Langkah - Langkah berikut :</p>
<b><p>- Fitur TAMBAH BARANG</p></b>
<p class="btn-danger"> * Perhatikan Kode Barang, Nama Barang, Harga Barang, Dan Stok Terisi.</p>
<b><p>- Fitur HAPUS BARANG</p></b>
<p class="btn-danger"> * Klik tombol HAPUS pada kolom tabel barang yang akan dihapus, maka barang akan terhapus.</p>
<p class="btn-danger"> * Klik tombol UBAH pada kolom tabel barang yang akan di ubah, lalu perhatikan Kode Barang, Nama Barang, Harga Barang, Dan Stok Terisi. Bila ingin menambahkan STOK BARANG maka hitunglah terlebih dahulu dari STOK YANG TERSISA dengan STOK TERKINI. Contoh : Stok tersisa 100, Stok terbaru 400 jadi 100 + 400 = 500, maka yang di input ke system adalah 500.</p>

<h4 class="btn-primary">Lihat Tagihan</h4>
<p>Terdapat fitur Lihat Tagihan, Ubah Tagihan, Riwayat Tagihan. Perhatikan Langkah - Langkah berikut :</p>
<b><p>- Fitur LIHAT TAGIHAN</p></b>
<p class="btn-danger"> * Klik kolom tanggal lalu setting tanggal dari tanggal berapa sampai tanggal berapa, setelah itu klik tombol LIHAT, maka laporan TAGIHAN akan muncul.</p>
<b><p>- Fitur UBAH TAGIHAN</p></b>
<p class="btn-danger"> * Klik tombol ubah di kolom tagihan yang akan di ubah, lau Perhatikan Nominal tagihan yang akan Di update Pastikan Nominal pembayaran Sesuai dan status angsuran sudah di update.</p>
<b><p>- Fitur RIWAYAT</p></b>
<p class="btn-danger"> * Jika di status pembayaran sudah LUNAS maka akan muncul TOMBOL RIWAYAT yang mana bisa melihat riwayat pembayaran si buyer</p>

<h4 class="btn-primary">Laporan Tagihan</h4>
<p>Terdapat fitur Laporan Penjualan yang bisa di lihat sesuai settingan tanggal. Perhatikan Langkah - Langkah berikut :</p>
<b><p>- Fitur TAMBAH BARANG</p></b>
<p class="btn-danger"> * Klik kolom tanggal lalu setting tanggal dari tanggal berapa sampai tanggal berapa, setelah itu klik tombol LIHAT, maka laporan akan muncul.</p>


